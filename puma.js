let login=document.getElementById("login")
console.log(login);
login.onclick=function(){
    let login1=document.getElementById("login1")
    console.log(login1);
    let res1=login1.classList.toggle("true")
    if(res1){
        login1.style.display="none"
    }else{
        login1.style.display="block"
    }
}

